     public void Listadopersonas() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // Limpia la tabla antes de agregar nuevos datos

    // Realiza la solicitud GET a la API
    String apiUrl = "http://localhost/APIenPHP/Obtener.php"; // Reemplaza con tu URL

    try {
        // Realiza la solicitud HTTP
        URL url = new URL(apiUrl);
        HttpURLConnection connection = (HttpURLConnection) url.openConnection();
        connection.setRequestMethod("GET");

        // Lee la respuesta JSON
        BufferedReader reader = new BufferedReader(new InputStreamReader(connection.getInputStream()));
        StringBuilder response = new StringBuilder();
        String line;
        while ((line = reader.readLine()) != null) {
            response.append(line);
        }
        reader.close();

        // Analiza la respuesta JSON
        JsonParser jsonParser = new JsonParser();
        JsonObject jsonResponse = jsonParser.parse(response.toString()).getAsJsonObject();
        JsonArray jsonArray = jsonResponse.getAsJsonArray("registros");

        // Recorre el arreglo JSON y agrega los datos a la tabla
        for (int i = 0; i < jsonArray.size(); i++) {
            JsonObject jsonObject = jsonArray.get(i).getAsJsonObject();
            String cedula = jsonObject.get("cedula").getAsString();
            String nombre = jsonObject.get("nombres").getAsString();
            String telefono = jsonObject.get("telefono").getAsString();
            String email = jsonObject.get("email").getAsString();

            // Agrega los datos a la tabla
            model.addRow(new Object[]{cedula, nombre, telefono, email});
        }
    } catch (IOException e) {
        e.printStackTrace();
        // Maneja la excepción aquí (puede ser un mensaje de error o alguna otra acción)
    }
}


